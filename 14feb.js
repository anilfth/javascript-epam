 // switch statement explanation
 const gadget = 'laptop'
switch(gadget){
    case 'laptop':
        console.log("laptop price is 900000")
        break;
        case 'smartphone':
            console.log("smartphone price is 80000")
            break;
            case 'charger':
                console.log("charger price is 1200")
                break;
                default:
                    console.log(`sorry ! ${gadget} is not available`)
                    break;

}